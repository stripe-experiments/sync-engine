#!/usr/bin/env node
/**
 * Generates src/versions.ts from the oas/ directory.
 *
 * SUPPORTED_API_VERSIONS = all *.json filenames in oas/ (sorted newest-first).
 * BUNDLED_API_VERSION    = the first (most recent) entry.
 *
 * Run directly:  node scripts/generate-versions.mjs
 * Run via build: pnpm --filter @stripe/sync-openapi build
 *
 * When adding a new bundled spec, drop the .json file into oas/ and re-run.
 * The same oas/ directory is served as the CDN source for the docs site.
 */
import { readdirSync, writeFileSync, mkdirSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const oasDir = join(__dirname, '..', 'oas')
const outFile = join(__dirname, '..', 'src', 'versions.ts')

const versions = readdirSync(oasDir)
  .filter((f) => f.endsWith('.json') && f !== 'manifest.json' && f !== 'index.html')
  .map((f) => f.replace(/\.json$/, ''))
  .sort()
  .reverse() // newest first

if (versions.length === 0) {
  console.error('No .json spec files found in oas/')
  process.exit(1)
}

const bundled = versions[0]
const lines = versions.map((v) => `  '${v}',`).join('\n')

mkdirSync(dirname(outFile), { recursive: true })
writeFileSync(
  outFile,
  `// Generated by scripts/generate-versions.mjs — do not edit manually.
// Source of truth: packages/openapi/oas/*.json
// Re-run \`pnpm --filter @stripe/sync-openapi build\` after adding a new bundled spec.

/** The most recent Stripe API version bundled in this package. */
export const BUNDLED_API_VERSION = '${bundled}' as const

/** All Stripe API versions bundled in this package, newest first. */
export const SUPPORTED_API_VERSIONS = [
${lines}
] as const satisfies readonly string[]
`
)

console.log(`Generated ${versions.length} version(s), bundled: ${bundled}`)
