import fs from 'node:fs'
import { createRequire } from 'node:module'
import path from 'node:path'

export interface GenerateSchemaOptions {
  apiVersion?: string
  specPath?: string
  output?: string
  schemaName?: string
}

interface StripeTypeProp {
  name: string
  optional: boolean
  rawType: string
}

function parseStripeTypeFile(
  content: string
): { interfaceName: string; properties: StripeTypeProp[] } | null {
  const allMatches = [...content.matchAll(/^ {4}interface (\w+) \{/gm)]
  if (allMatches.length === 0) return null

  const match = allMatches.find((m) => !m[1].startsWith('Deleted')) ?? allMatches[0]

  const interfaceName = match[1]
  const startPos = match.index! + match[0].length
  let depth = 1
  let pos = startPos
  while (pos < content.length && depth > 0) {
    if (content[pos] === '{') depth++
    else if (content[pos] === '}') depth--
    pos++
  }

  const body = content.substring(startPos, pos - 1)
  const properties: StripeTypeProp[] = []
  for (const line of body.split('\n')) {
    const propMatch = line.match(/^ {6}(\w+)(\??): (.+);$/)
    if (propMatch) {
      properties.push({
        name: propMatch[1],
        optional: propMatch[2] === '?',
        rawType: propMatch[3].trim(),
      })
    }
  }

  return { interfaceName, properties }
}

function mapToColumnConfig(
  rawType: string,
  optional: boolean
): { colType: string; nullable: boolean; tsType: string } | null {
  const hasNull = /\|\s*null\b/.test(rawType)
  const nullable = hasNull || optional
  const clean = rawType.replace(/\s*\|\s*null\b/g, '').trim()

  if (clean === 'void') return null
  if (clean === 'string' || /^'[^']*'$/.test(clean)) {
    return { colType: 'text', nullable, tsType: nullable ? 'string | null' : 'string' }
  }
  if (clean === 'number') {
    return { colType: 'numeric', nullable, tsType: nullable ? 'number | null' : 'number' }
  }
  if (clean === 'boolean') {
    return { colType: 'boolean', nullable, tsType: nullable ? 'boolean | null' : 'boolean' }
  }
  return {
    colType: 'jsonb',
    nullable,
    tsType: nullable ? 'Record<string, unknown> | null' : 'Record<string, unknown>',
  }
}

function generateSchemaFileContent(config: {
  entityName: string
  tableName: string
  properties: StripeTypeProp[]
  hasList: boolean
  hasRetrieve: boolean
  sdkPropName: string
}): string {
  const lines: string[] = []

  lines.push('// Auto-generated by generate-schema — do not edit manually')
  lines.push("import type Stripe from 'stripe'")
  lines.push("import { createEntity } from './lib/createEntity'")
  lines.push('')
  lines.push(
    `export const ${config.entityName} = createEntity<Stripe.${config.entityName}>('${config.tableName}', {`
  )

  for (const prop of config.properties) {
    const col = mapToColumnConfig(prop.rawType, prop.optional)
    if (!col) continue

    if (prop.name === 'id') {
      lines.push(`  id: { type: '${col.colType}', primary: true },`)
    } else if (col.nullable) {
      lines.push(`  ${prop.name}: { type: '${col.colType}', nullable: true },`)
    } else {
      lines.push(`  ${prop.name}: '${col.colType}',`)
    }
  }

  lines.push('})')

  if (config.hasList) {
    lines.push('')
    lines.push(
      `export function list(stripe: Stripe, params?: Stripe.${config.entityName}ListParams) {`
    )
    lines.push(`  return stripe.${config.sdkPropName}.list(params)`)
    lines.push('}')
  }

  if (config.hasRetrieve) {
    lines.push('')
    lines.push(`export function retrieve(stripe: Stripe, id: string) {`)
    lines.push(`  return stripe.${config.sdkPropName}.retrieve(id)`)
    lines.push('}')
  }

  lines.push('')
  return lines.join('\n')
}

export async function generateSchemaCommand(_options: GenerateSchemaOptions): Promise<void> {
  const _require = createRequire(import.meta.url)
  let stripeDir = path.dirname(_require.resolve('stripe'))
  while (path.basename(stripeDir) !== 'stripe') stripeDir = path.dirname(stripeDir)
  const stripeTypesDir = path.join(stripeDir, 'types')

  const objectNames = fs
    .readdirSync(stripeTypesDir)
    .filter((f) => f.endsWith('.d.ts') && !f.endsWith('Resource.d.ts'))
    .map((f) => f.replace('.d.ts', ''))
    .filter((f) => !['index', 'lib', 'shared'].includes(f))
    .map((name) =>
      name
        .replace(/([A-Z])/g, '_$1')
        .toLowerCase()
        .replace(/^_/, '')
    )

  objectNames.forEach((name) => console.log(name))
  console.log(`\nTotal: ${objectNames.length}`)

  const schemasDir = path.resolve(process.cwd(), 'schemas')
  fs.mkdirSync(schemasDir, { recursive: true })

  const typeFiles = fs
    .readdirSync(stripeTypesDir)
    .filter((f) => f.endsWith('.d.ts') && !f.endsWith('Resource.d.ts'))
    .map((f) => f.replace('.d.ts', ''))
    .filter((f) => !['index', 'lib', 'shared'].includes(f))

  let generated = 0
  for (const pascalPlural of typeFiles) {
    const content = fs.readFileSync(path.join(stripeTypesDir, `${pascalPlural}.d.ts`), 'utf-8')
    const parsed = parseStripeTypeFile(content)
    if (!parsed || parsed.properties.length === 0) continue

    const resourcePath = path.join(stripeTypesDir, `${pascalPlural}Resource.d.ts`)
    let hasList = false
    let hasRetrieve = false
    if (fs.existsSync(resourcePath)) {
      const resourceContent = fs.readFileSync(resourcePath, 'utf-8')
      hasList = /^\s+list\(/m.test(resourceContent)
      hasRetrieve = /^\s+retrieve\(/m.test(resourceContent)
    }

    const snakeName = pascalPlural
      .replace(/([A-Z])/g, '_$1')
      .toLowerCase()
      .replace(/^_/, '')
    const sdkPropName = pascalPlural.charAt(0).toLowerCase() + pascalPlural.slice(1)

    const fileContent = generateSchemaFileContent({
      entityName: parsed.interfaceName,
      tableName: snakeName,
      properties: parsed.properties,
      hasList,
      hasRetrieve,
      sdkPropName,
    })

    fs.writeFileSync(path.join(schemasDir, `${snakeName}.ts`), fileContent)
    console.log(`  Generated: schemas/${snakeName}.ts`)
    generated++
  }

  console.log(`\nGenerated ${generated} schema files in schemas/`)
}
